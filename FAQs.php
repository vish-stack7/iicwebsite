 <b>Q: Is the online registration mandatory to all the candidates applying for the PG/Master’s admission?</b>

 A: YES, online registration is mandatory to ALL the candidates applying for the postgraduate/Master’s courses in the University of Delhi.

 
 <b>Q.: I wish to seek admission only through the Direct (Merit) category. Do I need to apply through online registration?<br>     Am I expected to pay the Registration fee?</b>

 A: YES, as mentioned above, online registration is mandatory to ALL the candidates applying for the postgraduate/Master’s courses in the <br>    University of Delhi, irrespective of the mode of Intake. You will need to pay the Registration fee as well.

 
 <b>Q.: I would like to change my “Applicant’s details”, which I filled during the “New User registration”.Can I do that?</b>

 A: NO, you cannot change that information later on. Be careful in filling the information.

 
 <b>Q: I would like to change my Centre of Entrance Examination after completing the online registration form. Is it possible?</b>

 A: NO, Candidate shall choose any one of these seven cities for appearing in the entrance examination through the online registration.<br>    After completion of the online registration, the chosen Centre will not be changed.

 
 <b>Q: Is it possible to get the Admit Card through postal mode?</b>

 A: NO, the Admit Cards will only be generated online and will be available on your registration account.

 
 <b>Q: When will I be able to download the Admit Card? Is it immediately after filling the registration form?</b>

 A: Admit Card’s download link will be available in your online account after 24th May 2016 (closing date of online registration).<br>    Please visit the PG admission portal for news and updates related with the admission procedure.

 
 <b>Q: Shall I sign on the Candidate’s Signature area of the Admit Card before appearing for the Entrance Examination?</b>

 A: NO, the candidate is required to sign on the Admit Card in front of the invigilator during the Entrance Examination in order to<br>    verify the Candidate’s signature.

 
 <b>Q: Can I submit the fee through any other method, like demand draft etc?</b>

 A: NO, the registration fee is accepted only through the Online payment options available in the registration portal.

 
 <b>Q:  My internet got disconnected while filling the form. What should I do?</b>

 A:  When you get the internet connection, login again with your credentials and refill the information.

 
 <b>Q: How can I correct my mistake in the registration form? I have Uploaded the wrong Photo/ID Proof/ Signature?</b>

 A: NO modifications in the form are allowed once fee payment is made. Before making the online payment, you can edit your registration form.

 
 <b>Q: How can I apply in multiple courses? Do I need to make payment for other courses separately?</b>

 A: After completing your application for one Course, you can apply in another course using "Apply Now" button on your home area.<br>    You need to pay registration fee separately for each of the applied Courses.

 
 <b>Q: What does the “Final aggregate” mean to update result for merit? OR What percentage I need to fill in Update Result for admission<br>    under Merit?</b>

 A: Final Aggregate is the combined total of all the B.A/B.Com/B.Sc. (or equivalent) semesters (or annual) result together.

 
 <b>Q: I have uploaded the Final Aggregate Percentage, which has been accepted. But then I was asked to print the form and it still reads<br>    Result Awaited?</b>

 A: Please check the print of the form carefully; There is a row after Education Qualification with the result you have updated.

 
 <b>Q: What is the date of Entrance Examination for my course?</b>

 A: The date of Entrance Examination for different courses will be announced on the PG admission portal.

 
 <b>Q: How will I come to know the location of my Centre in the city I have chose?</b>

 A: The information related with the Centre will be announced on the PG admission portal. The information will also be printed on your<br>    Admit Card.

 
 <b>Q: Can you please tell me the syllabus and pattern for the Entrance Exam?</b>

 A: Please contact the respective Department of the University of Delhi for any Course specific query.

 
 <b>Q:  Can two candidates apply from the same account?</b>

 A: NO, only single candidate can apply from an account for PG Admissions.

 
 <b>Q: Do I need to send Hard Copy of the form?</b>

 A: NO, you don’t have to send Hard Copy of the form anywhere, if required you will be notified.

 
 <b>Q: How can I upload self attested ID Proof?</b>

 A: 1. Take a photocopy of your ID proof.

    2. Self attest it.

    3. Scan it and upload.
